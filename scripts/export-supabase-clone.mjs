import fs from "node:fs/promises";
import path from "node:path";

let postgres;
try {
  ({ default: postgres } = await import("postgres"));
} catch {
  throw new Error(
    "The optional clone exporter needs the postgres package. Run it with: npx -p postgres node scripts/export-supabase-clone.mjs"
  );
}

const dbUrl = process.env.DIRECT_URL || process.env.DATABASE_URL;
if (!dbUrl) {
  throw new Error("DIRECT_URL or DATABASE_URL is required");
}

const outFile = process.argv[2] || "clone-dumps/siakad_full_clone_with_auth.sql";
const sql = postgres(dbUrl, { ssl: "require", max: 1 });

const q = (ident) => `"${String(ident).replaceAll('"', '""')}"`;
const lit = (value) => {
  if (value === null || value === undefined) return "NULL";
  if (value instanceof Date) return `'${value.toISOString().replaceAll("'", "''")}'`;
  if (typeof value === "object") return `'${JSON.stringify(value).replaceAll("'", "''")}'`;
  return `'${String(value).replaceAll("'", "''")}'`;
};

const jsonLit = (value) => {
  if (value === null || value === undefined) return "NULL";
  return `'${JSON.stringify(value).replaceAll("'", "''")}'::jsonb`;
};

const lines = [];
const emit = (text = "") => lines.push(text);

async function getRows(query, params = []) {
  return sql.unsafe(query, params);
}

async function emitHeader() {
  emit("-- SIAKAD full clone dump generated from Supabase project qudjkcpudzngqzhkqccl");
  emit("-- Scope: public schema + public data + selected auth data + storage metadata.");
  emit("-- Restore target: fresh Supabase project. Storage blobs must be copied separately.");
  emit("\\set ON_ERROR_STOP on");
  emit("begin;");
  emit("create extension if not exists \"pgcrypto\" with schema \"extensions\";");
  emit("create extension if not exists \"uuid-ossp\" with schema \"extensions\";");
  emit("create extension if not exists \"pg_stat_statements\" with schema \"extensions\";");
  emit("create extension if not exists \"supabase_vault\" with schema \"vault\";");
  emit("create schema if not exists \"public\";");
  emit("create schema if not exists \"auth\";");
  emit("create schema if not exists \"storage\";");
  emit("set check_function_bodies = off;");
  emit("set session_replication_role = replica;");
}

async function emitSequences() {
  const seqs = await getRows(`
    select sequence_schema, sequence_name, data_type, start_value, minimum_value, maximum_value, increment
    from information_schema.sequences
    where sequence_schema = 'public'
    order by sequence_name
  `);
  for (const s of seqs) {
    emit(`create sequence if not exists ${q(s.sequence_schema)}.${q(s.sequence_name)} as ${s.data_type} start with ${s.start_value} increment by ${s.increment} minvalue ${s.minimum_value} maxvalue ${s.maximum_value};`);
  }
}

async function emitTables() {
  const tables = await getRows(`
    select tablename
    from pg_tables
    where schemaname = 'public'
    order by tablename
  `);

  for (const { tablename } of tables) {
    const cols = await getRows(`
      select
        a.attname,
        pg_catalog.format_type(a.atttypid, a.atttypmod) as type,
        a.attnotnull,
        pg_get_expr(ad.adbin, ad.adrelid) as default_expr,
        a.attidentity
      from pg_attribute a
      join pg_class c on c.oid = a.attrelid
      join pg_namespace n on n.oid = c.relnamespace
      left join pg_attrdef ad on ad.adrelid = a.attrelid and ad.adnum = a.attnum
      where n.nspname = 'public'
        and c.relname = $1
        and a.attnum > 0
        and not a.attisdropped
      order by a.attnum
    `, [tablename]);

    emit(`create table if not exists "public".${q(tablename)} (`);
    emit(cols.map((col) => {
      const parts = [`  ${q(col.attname)}`, col.type];
      if (col.attidentity === "a") parts.push("generated always as identity");
      if (col.attidentity === "d") parts.push("generated by default as identity");
      if (col.default_expr && !col.attidentity) parts.push(`default ${col.default_expr}`);
      if (col.attnotnull) parts.push("not null");
      return parts.join(" ");
    }).join(",\n"));
    emit(");");
  }
}

async function emitFunctions() {
  const funcs = await getRows(`
    select pg_get_functiondef(p.oid) as ddl
    from pg_proc p
    join pg_namespace n on n.oid = p.pronamespace
    where n.nspname = 'public'
    order by p.proname, pg_get_function_identity_arguments(p.oid)
  `);
  for (const f of funcs) emit(`${f.ddl};`);
}

async function emitConstraintsAndIndexes() {
  const constraints = await getRows(`
    select conname, conrelid::regclass::text as table_name, pg_get_constraintdef(oid) as ddl
    from pg_constraint
    where connamespace = 'public'::regnamespace
    order by case contype when 'p' then 1 when 'u' then 2 when 'c' then 3 when 'f' then 4 else 5 end, conname
  `);
  for (const c of constraints) {
    emit(`alter table ${c.table_name} drop constraint if exists ${q(c.conname)};`);
    emit(`alter table ${c.table_name} add constraint ${q(c.conname)} ${c.ddl};`);
  }

  const indexes = await getRows(`
    select indexdef
    from pg_indexes
    where schemaname = 'public'
      and indexname not in (
        select conname from pg_constraint where connamespace = 'public'::regnamespace
      )
    order by tablename, indexname
  `);
  for (const i of indexes) emit(`${i.indexdef};`);
}

async function emitTriggers() {
  const triggers = await getRows(`
    select pg_get_triggerdef(t.oid, true) as ddl
    from pg_trigger t
    join pg_class c on c.oid = t.tgrelid
    join pg_namespace n on n.oid = c.relnamespace
    where not t.tgisinternal
      and n.nspname in ('public','auth')
    order by n.nspname, c.relname, t.tgname
  `);
  for (const t of triggers) emit(`${t.ddl};`);
}

async function emitRlsAndPolicies() {
  const rlsTables = await getRows(`
    select n.nspname as schema_name, c.relname as table_name, c.relrowsecurity, c.relforcerowsecurity
    from pg_class c
    join pg_namespace n on n.oid = c.relnamespace
    where n.nspname = 'public' and c.relkind in ('r','p')
    order by c.relname
  `);
  for (const t of rlsTables) {
    if (t.relrowsecurity) emit(`alter table ${q(t.schema_name)}.${q(t.table_name)} enable row level security;`);
    if (t.relforcerowsecurity) emit(`alter table ${q(t.schema_name)}.${q(t.table_name)} force row level security;`);
  }

  const policies = await getRows(`
    select schemaname, tablename, policyname, permissive, roles, cmd, qual, with_check
    from pg_policies
    where schemaname = 'public'
    order by tablename, policyname
  `);
  for (const p of policies) {
    const roles = `{${p.roles.join(",")}}`;
    emit(`drop policy if exists ${q(p.policyname)} on ${q(p.schemaname)}.${q(p.tablename)};`);
    emit([
      `create policy ${q(p.policyname)} on ${q(p.schemaname)}.${q(p.tablename)}`,
      `as ${p.permissive}`,
      `for ${p.cmd}`,
      `to ${roles}`,
      p.qual ? `using (${p.qual})` : "",
      p.with_check ? `with check (${p.with_check})` : "",
      ";",
    ].filter(Boolean).join(" "));
  }
}

async function emitTableData(schema, table, where = "") {
  const cols = await getRows(`
    select column_name, data_type, udt_name
    from information_schema.columns
    where table_schema = $1 and table_name = $2
    order by ordinal_position
  `, [schema, table]);
  if (cols.length === 0) return;
  const rows = await getRows(`select * from ${q(schema)}.${q(table)} ${where}`);
  if (rows.length === 0) return;

  emit(`truncate table ${q(schema)}.${q(table)} cascade;`);
  const colList = cols.map((c) => q(c.column_name)).join(", ");
  for (const row of rows) {
    const vals = cols.map((c) => {
      const value = row[c.column_name];
      if (value === null || value === undefined) return "NULL";
      if (c.data_type === "json" || c.data_type === "jsonb") return jsonLit(value);
      if (c.data_type === "ARRAY") {
        return `array[${value.map(lit).join(", ")}]::${c.udt_name.replace(/^_/, "")}[]`;
      }
      return lit(value);
    }).join(", ");
    emit(`insert into ${q(schema)}.${q(table)} (${colList}) values (${vals});`);
  }
}

async function emitData() {
  const publicTables = await getRows(`
    select tablename
    from pg_tables
    where schemaname = 'public'
    order by tablename
  `);
  for (const { tablename } of publicTables) {
    await emitTableData("public", tablename);
  }

  const authTables = [
    "users",
    "identities",
    "sessions",
    "refresh_tokens",
    "mfa_amr_claims",
    "mfa_factors",
    "mfa_challenges",
    "one_time_tokens",
  ];
  for (const table of authTables) {
    await emitTableData("auth", table);
  }

  await emitTableData("storage", "buckets");
  await emitTableData("storage", "objects");
}

async function emitSequenceValues() {
  const seqs = await getRows(`
    select sequence_schema, sequence_name
    from information_schema.sequences
    where sequence_schema in ('public','auth')
    order by sequence_schema, sequence_name
  `);
  for (const s of seqs) {
    const [row] = await getRows(`select last_value, is_called from ${q(s.sequence_schema)}.${q(s.sequence_name)}`);
    emit(`select setval('${s.sequence_schema}.${s.sequence_name}', ${row.last_value}, ${row.is_called});`);
  }
}

async function emitFooter() {
  emit("set session_replication_role = origin;");
  emit("grant usage on schema public to anon, authenticated, service_role;");
  emit("grant all on all tables in schema public to anon, authenticated, service_role;");
  emit("grant all on all routines in schema public to anon, authenticated, service_role;");
  emit("grant all on all sequences in schema public to anon, authenticated, service_role;");
  emit("commit;");
}

try {
  await emitHeader();
  await emitSequences();
  await emitTables();
  await emitFunctions();
  await emitConstraintsAndIndexes();
  await emitData();
  await emitSequenceValues();
  await emitTriggers();
  await emitRlsAndPolicies();
  await emitFooter();

  await fs.mkdir(path.dirname(outFile), { recursive: true });
  await fs.writeFile(outFile, `${lines.join("\n")}\n`, "utf8");
  console.log(`Wrote ${outFile}`);
} finally {
  await sql.end();
}
